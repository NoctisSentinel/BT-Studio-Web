(function () {
  function resolve(keyPath) {
    return keyPath.split('.').reduce(function (obj, key) {
      return obj != null ? obj[key] : undefined;
    }, window.CONTENT);
  }

  function applyContent() {
    // Fill elements with data-t attribute
    document.querySelectorAll('[data-t]').forEach(function (el) {
      var val = resolve(el.getAttribute('data-t'));
      if (val == null) return;
      if (Array.isArray(val)) {
        el.innerHTML = val.map(function (item) { return '<li>' + item + '</li>'; }).join('');
      } else {
        el.innerHTML = val;
      }
    });

    // Page meta
    var m = window.CONTENT.meta;
    document.title = m.title;
    var descTag = document.querySelector('meta[name="description"]');
    if (descTag) descTag.setAttribute('content', m.description);

    // Dynamic hrefs
    var b = window.CONTENT.booking;
    document.querySelectorAll('[data-href="line"]').forEach(function (a) { a.href = b.line_url; });
    document.querySelectorAll('[data-href="email"]').forEach(function (a) { a.href = 'mailto:' + b.email; });
    document.querySelectorAll('[data-href="phone"]').forEach(function (a) { a.href = 'tel:' + b.phone.replace(/-/g, ''); });
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyContent);
  } else {
    applyContent();
  }
})();
